  static const double space8 = 8.0;   // Base unit
  static const double space12 = 12.0;  // 1.5x base unit
  static const double space16 = 16.0;  // 2x base unit, standard padding
  static const double space24 = 24.0;  // 3x base unit, for larger gaps
  static const double space32 = 32.0;  // 4x base unit
  static const double space48 = 48.0;  // 6x base unit
  static const double space64 = 64.0;  // 8x base unit
}
